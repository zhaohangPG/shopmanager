<script>
export default {
  created() {
    this.getData();
  },
  data() {
    return {
      query: "",
      pagenum: 1,
      pagesize: 2,
      list: [],
      tolal: -1,
      addFormVisble: false,
      addForm:{
          email:'',
      }
    };
  },
  methods: {
    handleSizeChange(val) {
      console.log(`每页 ${val} 条`);
      this.pagesize = val;
      this.pagenum = 1;
      this.getData();
    },
    handleCurrentChange(val) {
      console.log(`当前页: ${val}`);
      this.pagenum = val;
      this.getData();
    },
    getData() {
      const AUTH_TOKEN = localStorage.getItem("token");
      this.$http.defaults.headers.common["Authorization"] = AUTH_TOKEN;
      this.$http
        .get(
          `users?query=${this.query}&pagenum=${this.pagenum}&pagesize=${
            this.pagesize
          }`
        )
        .then(res => {
          console.log(res);
          const {
            data: {
              data: { users, total },
              meta: { status }
            }
          } = res;
          console.log(total);
          this.total = total;
          console.log(users, status);
          if (status === 200) {
            this.list = users;
          }
        });
    }
  }
};
</script>

